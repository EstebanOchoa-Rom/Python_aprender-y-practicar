"""
type
"""
# Type  es una operacion del sistema que permite saber el tipo de dato que estas utilizando 
# consultar el tipo de dato 
print (type("Hello, World!")) # el tipo de dato que se esta utilizando es str = string o cadena de texto
print (type(1)) # es un tipo de dato numerico 'int'
print (type(1.5)) # es un tipo de dato numerico 'float'
print (type(1 + 2j)) # es un tipo de dato numerico 'complex'
print (type([1,2,3])) # es un tipo de dato lista 'list'
print (type(True)) # es un tipo de dato booleano 'bool'
print (type({'name': 'John', 'age': 30})) # es un tipo de dato diccionario 'dict'
print (type({1.2,1.3,1.4})) # es un tipo de dato conjunto 'set'
print (type((1,2,3))) # es un tipo de dato tupla 'tuple'
# El type es para asegurrar  que el dato que estas utilizando es el correcto

